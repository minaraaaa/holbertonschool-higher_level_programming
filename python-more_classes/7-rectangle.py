#!/usr/bin/python3
"""
This module defines a Rectangle class with private attributes,
methods for area/perimeter, string representations, a destructor,
and class attributes to keep track of instances and print symbols.
"""


class Rectangle:
    """Representation of a rectangle."""

    number_of_instances = 0
    print_symbol = "#"

    def __init__(self, width=0, height=0):
        """Initializes the rectangle and increments instance count."""
        self.width = width
        self.height = height
        Rectangle.number_of_instances += 1

    @property
    def width(self):
        """Getter for the private instance attribute __width."""
        return self.__width

    @width.setter
    def width(self, value):
        """Setter for the private instance attribute __width."""
        if not isinstance(value, int):
            raise TypeError("width must be an integer")
        if value < 0:
            raise ValueError("width must be >= 0")
        self.__width = value

    @property
    def height(self):
        """Getter for the private instance attribute __height."""
        return self.__height

    @height.setter
    def height(self, value):
        """Setter for the private instance attribute __height."""
        if not isinstance(value, int):
            raise TypeError("height must be an integer")
        if value < 0:
            raise ValueError("height must be >= 0")
        self.__height = value

    def area(self):
        """Returns the rectangle area."""
        return self.__width * self.__height

    def perimeter(self):
        """Returns the rectangle perimeter."""
        if self.__width == 0 or self.__height == 0:
            return 0
        return 2 * (self.__width + self.__height)

    def __str__(self):
        """Returns the printable string representation of the rectangle."""
        if self.__width == 0 or self.__height == 0:
            return ""
        rect_str = []
        for i in range(self.__height):
            rect_str.append(str(self.print_symbol) * self.__width)
        return "\n".join(rect_str)

    def __repr__(self):
        """Returns a string representation of the rectangle for recreation."""
        return "Rectangle({:d}, {:d})".format(self.__width, self.__height)

    def __del__(self):
        """Prints a message and decrements instance count upon deletion."""
        print("Bye rectangle...")
        Rectangle.number_of_instances -= 1
